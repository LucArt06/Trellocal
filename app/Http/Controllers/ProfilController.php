    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        /**
         * we check that the data in the inputs are present
         * on verifie que les données dans les inputs sont bien présente
         */
        $request->validate([
            'name' => 'required|string|max:255',
            'email' => 'required|string|email|max:255|unique:users,email,' . auth()->user()->id,

        ]);
        // return message if their is success or error
        try {
            $users = User::findOrfail($id);
            $users->name = $request->input('name');
            $users->email = $request->input('email');
            $users->save();

            return redirect()->back()->with('success', 'Votre profil a bien été modifié.');
        } catch (\Exception $e) {

            return redirect()->back()->with('error', 'Une erreur est survenue, veuillez réessayer.');
        }
    }

